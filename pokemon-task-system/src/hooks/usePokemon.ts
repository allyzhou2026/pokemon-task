export function usePokemon() {
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const fetchPokemon = useCallback(async (id: number): Promise<Pokemon | null> => {
    // 检查缓存
    if (pokemonCache.has(id)) {
      return pokemonCache.get(id)!;
    }

    setLoading(true);
    setError(null);

    try {
      const response = await fetch(`${POKEAPI_BASE}/pokemon/${id}`);
      if (!response.ok) throw new Error('Failed to fetch pokemon');
      
      const data: Pokemon = await response.json();
      pokemonCache.set(id, data);
      return data;
    } catch (err: any) {
      setError(err.message);
      return null;
    } finally {
      setLoading(false);
    }
  }, []);

  const fetchPokemonSpecies = useCallback(async (id: number): Promise<PokemonSpecies | null> => {
    // 检查缓存
    if (speciesCache.has(id)) {
      return speciesCache.get(id)!;
    }

    setLoading(true);
    setError(null);

    try {
      const response = await fetch(`${POKEAPI_BASE}/pokemon-species/${id}`);
      if (!response.ok) throw new Error('Failed to fetch pokemon species');
      
      const data: PokemonSpecies = await response.json();
      speciesCache.set(id, data);
      return data;
    } catch (err: any) {
      setError(err.message);
      return null;
    } finally {
      setLoading(false);
    }
  }, []);

  const fetchRandomPokemon = useCallback(async (): Promise<Pokemon | null> => {
    // 随机生成初代宝可梦 1-151
    const randomId = Math.floor(Math.random() * 151) + 1;
    return fetchPokemon(randomId);
  }, [fetchPokemon]);

  // 周五特殊精灵ID列表（有代表性的初代精灵）
  const FRIDAY_SPECIAL_POKEMON = [
    25,  // 皮卡丘
    4,   // 小火龙
    7,   // 杰尼龟
    1,   // 妙蛙种子
    6,   // 喷火龙
    9,   // 水箭龟
    3,   // 妙蛙花
    150, // 超梦
    149, // 快龙
    130, // 暴鲤龙
    131, // 拉普拉斯
    143, // 卡比兽
    133, // 伊布
  ];

  // 获取精灵（周五特殊处理）
  const fetchPokemonForDay = useCallback(async (date: string): Promise<Pokemon | null> => {
    const dayOfWeek = new Date(date).getDay();
    const isFriday = dayOfWeek === 5; // 周五

    if (isFriday) {
      // 周五：从特殊精灵列表中随机选择
      const randomIndex = Math.floor(Math.random() * FRIDAY_SPECIAL_POKEMON.length);
      const specialId = FRIDAY_SPECIAL_POKEMON[randomIndex];
      return fetchPokemon(specialId);
    } else {
      // 其他天：随机获取初代宝可梦
      return fetchRandomPokemon();
    }
  }, [fetchPokemon, fetchRandomPokemon]);

  const getPokemonDescription = useCallback((species: PokemonSpecies | null, pokemonId?: number): string => {
    // 优先使用中文描述映射
    if (pokemonId && pokemonDescriptions[pokemonId]) {
      return pokemonDescriptions[pokemonId];
    }
    
    if (!species) return '这是一只神秘的宝可梦，等待着被训练家发现！';
    
    // 尝试获取中文描述
    const chineseEntry = species.flavor_text_entries.find(
      entry => entry.language.name === 'zh-Hans' || entry.language.name === 'zh'
    );
    
    if (chineseEntry) {
      return chineseEntry.flavor_text.replace(/\n|\f/g, ' ');
    }
    
    return '这是一只神秘的宝可梦，等待着被训练家发现！';
  }, []);

  const getPokemonImage = useCallback((pokemon: Pokemon | null): string => {
    if (!pokemon) return '';
    
    // 优先使用官方艺术作品
    return pokemon.sprites.other?.['official-artwork']?.front_default || 
           pokemon.sprites.front_default ||
           '';
  }, []);

  const capitalizeFirst = useCallback((str: string): string => {
    return str.charAt(0).toUpperCase() + str.slice(1);
  }, []);

  // 获取宝可梦中文名字
  const getPokemonName = useCallback((pokemonId: number, englishName?: string): string => {
    if (pokemonNames[pokemonId]) {
      return pokemonNames[pokemonId];
    }
    return englishName ? capitalizeFirst(englishName) : '未知宝可梦';
  }, [capitalizeFirst]);

  return {
    loading,
    error,
    fetchPokemon,
    fetchPokemonSpecies,
    fetchRandomPokemon,
    fetchPokemonForDay,
    getPokemonDescription,
    getPokemonImage,
    capitalizeFirst,
    getPokemonName,
    pokemonNames,
  };
}
